class ILSVRC2012Dataset(dataset.Dataset, instrumentation.Observable):
    def __init__(self, base_path, side_length, use_lazy_mode=True):
        instrumentation.Observable.__init__(self)
        self.base_path = base_path
        if not os.path.exists(base_path):
            raise ValueError(f"ILSVRC2012 dataset path does not exist: {base_path}")

        self.side_length = side_length
        self.use_lazy_mode = use_lazy_mode

        self.wordnet_ids = [x.strip() for x in _leaf_synsets]

        # To only display warning once
        self.display_not_found_warning = False

    def setup(self, **kwargs):
        pass

    def train_pool_count(self):
        return 1

    def test_pool_count(self):
        return 1

    def train_pool(self, index, label_resource_id):
        assert index == 0
        return self.get_training_pool(label_resource_id)

    def test_pool(self, index, label_resource_id):
        assert index == 0
        return self.get_validation_pool(label_resource_id)

    def namespace(self):
        return _namespace_uid

    def get_training_pool(self, label_resource_id):
        return self._build_pool_from_directory("train", label_resource_id)

    def get_validation_pool(self, label_resource_id):
        return self._build_pool_from_directory("val", label_resource_id)

    def _build_pool_from_directory(self, directory, label_resource_id):
        pool_samples = []
        for wordnet_id in self.wordnet_ids:
            synset_name = _wordnet_id_to_synset[wordnet_id]
            synset_path = os.path.join(
                self.base_path, f"ILSVRC2012_img_{directory}", wordnet_id
            )
            if os.path.exists(synset_path):
                for i, filename in enumerate(os.listdir(synset_path)):
                    pool_samples += [
                        self._build_sample(
                            os.path.join(synset_path, filename),
                            i,
                            label_resource_id,
                            synset_name,
                            directory,
                        )
                    ]
            else:
                if not self.display_not_found_warning:
                    self.log_warning(f"{synset_path} not found, returning empty pool.")
                self.display_not_found_warning = True

        return pool_samples

    def _build_sample(self, filename, i, label_resource_id, synset_name, split):
        sample_ = data.Sample(
            source=self.__class__.__name__,
            uid=f"{_namespace_uid}::{split}:{synset_name}:{i}",
        ).add_resource(
            self.__class__.__name__, label_resource_id, f"WordNet3.0::{synset_name}"
        )
        if self.use_lazy_mode:
            sample_ = sample_.add_resource(
                self.__class__.__name__, "image_location", str(filename)
            ).add_lazy_resource(
                self.__class__.__name__, "input_img_np", self._load_from_location
            )
        else:
            sample_ = sample_.add_resource(
                self.__class__.__name__, "image_location", str(filename)
            )
            sample_ = sample_.add_resource(
                self.__class__.__name__,
                "input_img_np",
                self._load_from_location(sample_),
            )
        return sample_

    def _load_from_location(self, sample_):
        im = NetworkResistantImage.open(
            sample_.get_resource("image_location"), self
        ).resize((self.side_length, self.side_length), Image.ANTIALIAS)
        if im.mode != "RGB":
            im = im.convert("RGB")
        return np.asarray(im)

    def get_hyponymy_relation_source(self):
        return relation.StaticRelationSource([])

    def prediction_targets(self):
        return [
            f"{_namespace_uid}::{_wordnet_id_to_synset[wordnet_id]}"
            for wordnet_id in self.wordnet_ids
        ]
